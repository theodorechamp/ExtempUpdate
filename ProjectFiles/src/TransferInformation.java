
public class TransferInformation {
	boolean AlJazeera;
	boolean BBC;
	boolean NPR;
	String location;
	
	public boolean isAlJazeera() {
		return AlJazeera;
	}
	public void setAlJazeera(boolean alJazeera) {
		AlJazeera = alJazeera;
	}
	public boolean isBBC() {
		return BBC;
	}
	public void setBBC(boolean bBC) {
		BBC = bBC;
	}
	public boolean isNPR() {
		return NPR;
	}
	public void setNPR(boolean nPR) {
		NPR = nPR;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	
}
